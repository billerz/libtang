#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	c = c % 256;
	while (*s != '\0')
	{
		if (*s == c)
			return ((char *) s);
		s++;
	}
	if (*s == c)
		return ((char *) s);
	return (NULL);
}
